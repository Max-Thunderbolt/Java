    private void ProcessReportMenuItem_ToolsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProcessReportMenuItem_ToolsActionPerformed

        String agentName = null;
        double price = 0, commission = 0, calcCommission = 0;
        String location = LocationComboBox.getItemAt(LocationComboBox.getSelectedIndex());

        // Check to see if fields are populated
        if (validName() && validPrice() && validPercentage()) {
            agentName = AgentNameTextField.getText();
            price = Double.parseDouble(PropertyPriceTextField.getText());
            commission = Double.parseDouble(CommissionPercentageTextField.getText());
            calcCommission = (commission / 100) * price;

            String report = "ESATE LOCATION: \t" + location;
            report += "\nAGENT NAME: \t\t" + agentName;
            report += "\nPROPERTY PRICE: \tR " + price;
            report += "\nCOMMISSION PERCENTAGE: \tR " + commission + "%";
            report += "\nCALCULATED COMMISSION: \tR " + calcCommission;

            EstateAgentReportTextArea.setText(report);
        } else {
            EstateAgentReportTextArea.setText("ERROR: Enter blank fields!!!");
        }
    }//GEN-LAST:event_ProcessReportMenuItem_ToolsActionPerformed

