    private void SaveReportMenuItem_ToolsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveReportMenuItem_ToolsActionPerformed

        if (!validName() && !validPrice() && !validPercentage()) {
            EstateAgentReportTextArea.setText("ERROR: Enter blank fields!!!");
        } else {
            // Create a file named report.txt
            try {
                File obj = new File("report.txt");
                if (obj.createNewFile()) {
                    System.out.println("File created, " + obj.getName());
                } else {
                    System.out.println("There was an error.");
                }
            } catch (IOException e) {
                System.out.println("An error occurred.");
                e.printStackTrace();
            }

            // Write to the file
            try {
                double calcCommission = (Double.parseDouble(CommissionPercentageTextField.getText()) / 100) * (Double.parseDouble(PropertyPriceTextField.getText()));
                String report = "ESTATE AGENT REPORT\n********************************";
                report += "\nESATE LOCATION: \t" + LocationComboBox.getItemAt(LocationComboBox.getSelectedIndex());
                report += "\nAGENT NAME: \t\t" + AgentNameTextField.getText();
                report += "\nPROPERTY PRICE: \tR " + PropertyPriceTextField.getText();
                report += "\nCOMMISSION PERCENTAGE: \tR " + CommissionPercentageTextField.getText() + "%";
                report += "\nCALCULATED COMMISSION: \tR " + calcCommission;
                report += "\n*******************************";

                FileWriter writer = new FileWriter("report.txt");
                writer.write(report);
                writer.close();
                System.out.println("Data saved to file.");
            } catch (IOException e) {
                System.out.println("An error occurred.");
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_SaveReportMenuItem_ToolsActionPerformed

